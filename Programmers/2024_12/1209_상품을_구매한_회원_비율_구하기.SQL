SELECT YEAR(SALES_DATE) AS YEAR, MONTH(SALES_DATE) AS MONTH,
COUNT(DISTINCT(O.USER_ID)) AS PURCHASED_USERS, ROUND(COUNT(DISTINCT(O.USER_ID))/TOTAL,1) AS PUCHASED_RATIO
FROM (SELECT COUNT(*) AS TOTAL
FROM USER_INFO
WHERE YEAR(JOINED)=2021) T JOIN ONLINE_SALE O
JOIN USER_INFO U
ON U.USER_ID=O.USER_ID
WHERE YEAR(U.JOINED)=2021
GROUP BY YEAR(SALES_DATE), MONTH(SALES_DATE)
ORDER BY YEAR, MONTH